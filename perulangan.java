/*
 * Soal no. 3 perulangan bilangan genap dari 1 - 100
 */
public class perulangan {

    public static void main(String[] args) {
        // deklarasi variabel
        int i;
        // perulangan dari 0 sampai 100
        for (i = 0; i <= 100; i++) {
            System.out.println("i = " + i);
        }


        // deklarasi variabel
        int j;
        // perulangan dari 0 sampai 11 menggunakan while
        j = 0;
        while (j <= 100) {
            System.out.println("j = " + j);
            j++;
        }
    }
}